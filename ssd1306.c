static char cx, cy;

static void ssd1306_cmd(char cmd) {
    char buff[2];
    buff[0] = SSD1306_COMMAND;
    buff[1] = cmd;  
    i2c_write(SSD1306_ADDRESS, buff, 2);
}

void ssd1306_init() {
    ssd1306_cmd(SSD1306_DISPLAY_OFF);
    ssd1306_cmd(SSD1306_CHARGE_PUMP);
    ssd1306_cmd(0x14);
    ssd1306_cmd(SSD1306_MEMORY_ADDR_MODE);
    ssd1306_cmd(0x00);
    ssd1306_cmd(SSD1306_DISPLAY_ON);

    ssd1306_cmd(SSD1306_SET_DISPLAY_CLOCK_DIV_RATIO);
    ssd1306_cmd(0xF0);

    cx = 0;
    cy = 0;
}

void ssd1306_clear(char color) {
    int i;
    char buff[2];

    buff[0] = SSD1306_DATA_CONTINUE;
    if (color)
    	buff[1] = 0xff;
    else
    	buff[1] = 0x00;

    ssd1306_setcursor(0, 0);

    for (i = 0; i < 128 * 8; ++i)
        i2c_write(SSD1306_ADDRESS, buff, 2);
}

void ssd1306_setcursor(char page, char column) {
	ssd1306_cmd(SSD1306_SET_PAGE_ADDR);
    ssd1306_cmd(page);
    ssd1306_cmd(7);
    ssd1306_cmd(SSD1306_SET_COLUMN_ADDR);
    ssd1306_cmd(column);
    ssd1306_cmd(127);
}

void ssd1306_putc(char c) {
	int i;
	char buff[2];
	buff[0] = SSD1306_DATA_CONTINUE;

	if (c == '\n' || c == '\r') {
		++cy;
		cx = 0;
		return;
	}
	ssd1306_setcursor(cy, cx * 6);

	for (i = 0; i < 5; ++i) {	
		buff[1] = font[((int)(c - 32) * 5) + i];
		i2c_write(SSD1306_ADDRESS, buff, 2);
	}

	if (++cx > 127) {
		cx = 0;
		++cy;
	}
}

void ssd1306_print(char *s) {
	while (*s)
		ssd1306_putc(*s++);
}

void ssd1306_write(char page, char column, char byte) {
    char buff[2];

    buff[0] = SSD1306_DATA_CONTINUE;
    buff[1] = byte;

    ssd1306_setcursor(page, column);
    i2c_write(SSD1306_ADDRESS, buff, 2);
}

